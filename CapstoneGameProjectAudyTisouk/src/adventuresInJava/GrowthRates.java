package adventuresInJava;

//Growth and Experience
public class GrowthRates {

	private int hp;
	private int strength;
	private int magic;
	private int skill;
	private int speed;
	private int luck;
	private int defense;
	private int resistance;
	
	
	
	
	
	public GrowthRates(int hp, int strength, int magic, int skill, int speed,
            int luck, int defense, int resistance) {
			this.hp = hp;
			this.strength = strength;
			this.magic = magic;
			this.skill = skill;
			this.speed = speed;
			this.luck = luck;
			this.defense = defense;
			this.resistance = resistance;

	}

	public int getHp() {
		
			return hp;
	}
	
	public int getStrength() {
		
		return strength;
	}

	
	public int getMagic() {
		
		return magic;
	}

	public int getSkill() {
		
		return skill;
	}

	public int getSpeed() {
		
		return speed;
	}

	public int getLuck() {
		
		return luck;
	}
	
	public int getDefense() {
		
		return defense;
	}

	public int getResistance() {
		
		return resistance;
	}





}
